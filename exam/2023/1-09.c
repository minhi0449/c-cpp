// 2023년 1회 기출문제 9번
// 2진수를 10진수로 변환하는 프로그램

#include <stdio.h>
int main(){
    // 변환할 이진수를 정수로 저장 (실제로는 101110 이라는 숫자)
    int input = 101110;

    // 각 자릿수의 가중치 (2^0=1 부터 시작해서 2배씩 증가)
    int di = 1;

    // 최종 삼진수 결과를 저장할 변수
    int sum = 0;

    // 무한 반복 : 모든 자릿수를 처리할 때까지 계속
    while(1){

        // 모든 자릿수를 다 처리했으면 반복문 종료
        if(input == 0) break;

        else{
            // 핵심 계산 : (가장 오른쪽 자릿수) * (현재 가중치)를 합계에 더함
            // input % 10 : 가장 오른쪽 자릿수 추출
            // di : 현재 자릿수의 가중치 (1, 2, 4, 8, 16, 32 ...)
            sum = sum + (input % 10) * di;

            // 다음 자릿수를 위해 가중치를 2배로 증가 (이진수의 특성)
            di = di * 2;

            // 처리한 오른쪽 자릿수를 제거 (정수 나눗셈으로 한 자리씩 제거)
            input = input / 10;
        }
    }

    printf("%d", sum);
    return 0;
}

// 출력 결과 : 46