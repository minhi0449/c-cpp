// 두 문자열에서 공통 문자 찾기

#include <stdio.h>
int main(){
    char* a = "qwer";
    char* b = "qwety";
    int i, j;
    for(i = 0; a[i] != '\0'; i++){
        for(j = 0; b[j] != '\0'; j++){

            // 조건문 : 현재 비교 중인 두 문자가 같은지 확인
            // a[i] = 첫 번째 문자열 a의 i번째 문자
            // b[j] = 두 번째 문자열 b의 j번째 문자
            if(a[i] == b[j]){

                // 같은 문자를 발견하면 해당 문자를 화면에 출력
                // %c: 문자 하나를 출력하는 서식 지정자
                printf("%c", a[i]);
            }
        }
    }
    return 0;
}

/*
    실행 과정:

    i=0일 때 (a[0]='q')

    j=0: 'q' vs 'q' → 같음! → 'q' 출력
    j=1: 'q' vs 'w' → 다름
    j=2: 'q' vs 'e' → 다름
    j=3: 'q' vs 't' → 다름
    j=4: 'q' vs 'y' → 다름


    i=1일 때 (a[1]='w')

    j=0: 'w' vs 'q' → 다름
    j=1: 'w' vs 'w' → 같음! → 'w' 출력
    j=2: 'w' vs 'e' → 다름
    j=3: 'w' vs 't' → 다름
    j=4: 'w' vs 'y' → 다름


    i=2일 때 (a[2]='e')

    j=0: 'e' vs 'q' → 다름
    j=1: 'e' vs 'w' → 다름
    j=2: 'e' vs 'e' → 같음! → 'e' 출력
    j=3: 'e' vs 't' → 다름
    j=4: 'e' vs 'y' → 다름


    i=3일 때 (a[3]='r')

    j=0~4: 모두 다름 (출력 없음)


    최종 출력 결과: qwe
*/